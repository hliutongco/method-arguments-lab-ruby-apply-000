#The introduction method takes in the name argument
#It puts out a string that includes the name argument through string interpolation

def introduction(name)
  puts "Hi, my name is #{name}."
end

#The introduction_with_language method takes in the name and language arguments
#It puts out a string that includes both arguments through string interpolation

def introduction_with_language(name, language)
  puts "Hi, my name is #{name} and I am learning to program in #{language}."
end
